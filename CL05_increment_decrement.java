public class CL05_increment_decrement {
    public static void main(String[] args) {
        int i=10;
        int j=2;
        System.out.println(i++); // This is the postfix means first the value will print then increment
        System.out.println(i);
        System.out.println(++j); // This is the prifix means 1st the increment will happen then value is printeds
        System.out.println(j);

    }
}
