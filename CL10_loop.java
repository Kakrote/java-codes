/**
 * CL10_loop
 * this is class to learn loops 
 * While loop
 */
public class CL10_loop {

    public static void main(String[] args) {
        //While loop ()
        int i=100;
        while (i<=200) {
            System.out.println(i++);// i++ means continue the increment in i untill the condition gets false
        }
        System.out.println("End of while loop");
    }
}